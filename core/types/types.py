print(type(None))
# Prints <class 'NoneType'>

print(type(True))
# Prints <class 'bool'>

print(type(5))
# Prints <class 'int'>

print(type(5.5))
# Prints <class 'float'>

print(type(5 + 5j))
# Prints <class 'complex'>

print(type('Hello World!'))
# Prints <class 'str'>

print(type([1, 2, 3]))
# Prints <class 'list'>

print(type((1, 2, 3)))
# Prints <class 'tuple'>

print(type({1, 2, 3}))
# Prints <class 'set'>

print(type(frozenset({1, 2, 3})))
# Prints <class 'frozenset'>

print(type({'name': 'John Doe'}))
# Prints <class 'dict'>

print(type(range(5)))
# Prints <class 'range'>

print(type(type))
# Prints <class 'type'>